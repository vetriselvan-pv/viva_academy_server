CREATE TYPE STATUS_TYPE AS ENUM('ACTIVE', 'INACTIVE');

CREATE TABLE BRANCHES (
    BRANCH_ID SERIAL PRIMARY KEY,
    BRANCH_NAME VARCHAR(50) NOT NULL,
    ADDRESS VARCHAR(150) NOT NULL,
    CITY VARCHAR(20) NOT NULL,
    PHONE TEXT NOT NULL CHECK(PHONE ~ '^\+?[1-9]\d{1,14}$'),
    EMAIL CITEXT NOT NULL CHECK(EMAIL ~ '^[A-Za-z0-9._%-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,4}$'),
    STATUS STATUS_TYPE NOT NULL DEFAULT 'ACTIVE' ,
    CREATED_AT TIMESTAMPTZ 
);

CREATE TABLE ROLES (
    ROLE_ID VARCHAR(10) PRIMARY KEY,
    ROLE_NAME VARCHAR(25) NOT NULL,
    DESCRIPTION TEXT
);

CREATE TABLE PERMISSIONS (
    PERMISSION_ID VARCHAR(10) PRIMARY KEY,
    PERMISSION_NAME VARCHAR(30) NOT NULL,
    MODULE VARCHAR(40) NOT NULL
);

CREATE TYPE STUDENTS_GENDER AS ENUM ('MALE','FEMALE','NOT_MENTIONED');

CREATE TABLE STUDENTS (
    STUDENT_ID VARCHAR(30) PRIMARY KEY,
    NAME VARCHAR(100) NOT NULL,
    GENDER STUDENTS_GENDER NOT NULL,
    DOB TIMESTAMP NOT NULL,
    MOBILE TEXT NOT NULL,
    EMAIL CITEXT
);

CREATE TYPE COURSE_STATUS AS ENUM ('TODO', 'PROGRESS', 'COMPLETED', 'CANCELLED');

CREATE TABLE COURSES  (
    COURSE_ID SERIAL PRIMARY KEY,
    COURSE_NAME VARCHAR(100) NOT NULL,
    DURATION INT NOT NULL,
    FEE_AMOUNT INT NOT NULL,
    STATUS COURSE_STATUS NOT NULL DEFAULT 'TODO'
);

CREATE TABLE ROLE_PERMISSION(
    ROLE_PERMISSION_ID SERIAL PRIMARY KEY,
    ROLE_ID SERIAL REFERENCES ROLES(ROLE_ID),
    PERMISSION_ID VARCHAR(10) REFERENCES PERMISSIONS(PERMISSION_ID)
);

CREATE TYPE USER_STATUS AS ENUM ('ACTIVE', 'INACTIVE', 'FREEZE');

CREATE TABLE USERS (
    USER_ID VARCHAR(15) PRIMARY KEY,
    BRANCH_ID SERIAL REFERENCES BRANCHES(BRANCH_ID),
    ROLE_ID SERIAL REFERENCES ROLES(ROLE_ID),
    NAME VARCHAR(40) NOT NULL ,
    EMAIL CITEXT NOT NULL CHECK(EMAIL ~ '^[A-Za-z0-9._%-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,4}$'),
    MOBILE TEXT NOT NULL,
    PASSWORD_HASH VARCHAR(255) NOT NULL ,
    STATUS USER_STATUS NOT NULL DEFAULT 'ACTIVE',
    LAST_LOGIN TIMESTAMPTZ NOT NULL,
    CREATED_AT TIMESTAMPTZ
);


CREATE TABLE BATCHES (
    BATCH_ID SERIAL PRIMARY KEY,
    COURSE_ID SERIAL REFERENCES COURSES(COURSE_ID),
    BRANCH_ID SERIAL REFERENCES BRANCHES(BRANCH_ID),
    -- FACULTY_ID SERIAL REFERENCES 
    BATCH_NAME VARCHAR(100) NOT NULL,
    START_DATE TIMESTAMPTZ NOT NULL,
    END_DATE TIMESTAMPTZ ,
    CAPACITY INT 
);



CREATE TABLE STUDENT_COURSES (
    STUDENT_ID VARCHAR(30) REFERENCES STUDENTS(STUDENT_ID) NOT NULL,
    BRANCH_ID  SERIAL REFERENCES BRANCHES(BRANCH_ID) NOT NULL,
    COURSE_ID SERIAL REFERENCES COURSES(COURSE_ID) NOT NULL,
    BATCH_ID SERIAL REFERENCES BATCHES(BATCH_ID) NOT NULL,
    ADMISSION_DATE TIMESTAMP NOT NULL, 
    REMARKS VARCHAR(100)
);

CREATE TABLE STUDENT_DOCUMENTS (
    DOCUMENT_ID SERIAL PRIMARY KEY,
    STUDENT_ID VARCHAR(30) REFERENCES STUDENTS(STUDENT_ID) NOT NULL,
    DOCUMENT_TYPE VARCHAR(10) NOT NULL,
    FILE TEXT NOT NULL
);

CREATE TABLE STUDENTS_FEE_PLANS (
    PLAN_ID VARCHAR(20) PRIMARY KEY,
    STUDENT_ID VARCHAR(30) REFERENCES STUDENTS(STUDENT_ID),
    TOTAL_FEE NUMERIC(12,2) NOT NULL,
    DISCOUNT NUMERIC(12,2) NOT NULL,
    NET_FEE NUMERIC(12,2) NOT NULL
);

CREATE TYPE STUDENT_FEE_INSTALLMENT_STATIUS AS ENUM ('PENDING','COMPLETED','REFUNDED');

CREATE TABLE STUDENT_FEE_INSTALLMENT(
    INSTALLMENT_ID VARCHAR(20) PRIMARY KEY,
    PLAN_ID VARCHAR(20) REFERENCES STUDENTS_FEE_PLANS(PLAN_ID),
    DUE_DATE TIMESTAMP NOT NULL,
    AMOUNT NUMERIC(12,2) NOT NULL,
    STATUS STUDENT_FEE_INSTALLMENT_STATIUS NOT NULL DEFAULT 'PENDING'
);

CREATE TYPE ENQUIRY_STATUS AS ENUM ('ENQUIRED', 'INPROGRESS', 'CANCELLED', 'COMPLETED');

CREATE TABLE ENQUIRIES (
    ENQUIRY_ID SERIAL PRIMARY KEY,
    BRANCH_ID SERIAL REFERENCES BRANCHES(BRANCH_ID),
    LEAD_SOURCE VARCHAR(50) NOT NULL ,
    STUDENT_NAME VARCHAR(100) NOT NULL,
    MOBILE TEXT NOT NULL CHECK(MOBILE ~ '^\+?[1-9]\d{1,14}$'),
    EMAIL CITEXT NOT NULL CHECK(EMAIL ~ '^[A-Za-z0-9._%-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,4}$'),
    COURSE_ID VARCHAR(20) REFERENCES COURSES(COURSE_ID),
    STATUS ENQUIRY_STATUS NOT NULL,
   -- ASSIGNED_TO 
   NEXT_FOLLOW_UP TIMESTAMPTZ NOT NULL,
   REMARKS TEXT ,
    CREATED_AT TIMESTAMPTZ NOT NULL
);

